pub struct Ast<'src> {
    ast: ArenaBox<generated_ast::Module>,
    parse_errors: Vec<ParseErr>,
    ast_arena: Arena,
    symbols: SymbolFactory<'src>,
    source_holder: SourceHolder<'src>,
}

impl<'src> Ast<'src> {
    pub fn new(
        ast: ArenaBox<generated_ast::Module>,
        arena: Arena,
        source_holder: SourceHolder<'src>,
        symbols: SymbolFactory<'src>,
        errors: Vec<ParseErr>,
    ) -> Self {
        Self {
            ast,
            parse_errors: errors,
            ast_arena: arena,
            symbols,
            source_holder,
        }
    }

    pub fn success(&self) -> bool {
        self.parse_errors.is_empty()
    }

    pub fn errors(&self) -> &Vec<ParseErr> {
        &self.parse_errors
    }
}

impl<'src> Display for Ast<'src> {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        let mut printer = ASTPrinter {
            symbols: &self.symbols,
            arena: &self.ast_arena,
            source_holder: &self.source_holder,
            writer: f,
        };
        printer.visit_Module(self.ast.get(&self.ast_arena))
    }
}

